//! OBJETO MATH
console.log(Math);

//* Impresión de PI
console.log(Math.PI);

//* Valor absoluto de un numero
console.log(Math.abs(-7.8));

//* Redondear numeros
console.log(Math.ceil(7.8)); //Redondea al numero mayor siempre
console.log(Math.floor(7.8)); // Redondea al numero menor siempre
console.log(Math.round(7.2)); // Redondea al numero más proximo
console.log(Math.round(7.8)); // Redondea al numero más proximo

//* Raiz cuadrada de un numero
console.log(Math.sqrt(81));

//* Elevación de potencias
console.log(Math.pow(2,5));

//* Devolución de positivo o negativo
console.log(Math.sign(-80));
console.log(Math.sign(0));
console.log(Math.sign(80));

//* Valor aleatorio comprendido entre 0 y 1
console.log(Math.random());
console.log(Math.random());
console.log(Math.random());
console.log(Math.round(Math.random() * 1000));
console.log(Math.round(Math.random() * 1000));
console.log(Math.round(Math.random() * 1000));